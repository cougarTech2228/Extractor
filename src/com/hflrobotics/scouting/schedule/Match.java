package com.hflrobotics.scouting.schedule;

public class Match
{
	
	public int match;
	public int r1;
	public int r2;
	public int r3;
	public int b1;
	public int b2;
	public int b3;
	
	
	public Match(int match, int r1, int r2, int r3, int b1, int b2, int b3)
	{
		this.match = match;
		this.r1 = r1;
		this.r2 = r2;
		this.r3 = r3;
		this.b1 = b1;
		this.b2 = b2;
		this.b3 = b3;
	}	
}
